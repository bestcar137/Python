# 실습결과보고서 문제 4번

"""
4. 두 개의 수를 받아 오름차순으로 반환하는 함수 getSorted(x, y)를 작성하고 테스트하시오.
- 함수는 (작은값, 큰값)을 '두 값'으로 반환한다(다중 반환/튜플).
- 메인 코드에서는 언패킹[a, b = getSorted(x, y)]을 활용한다.
- 입력: 프롬프트 메시지 없이 공백으로 구분된 두 정수(예: 30 20)
- 출력(3줄):
  첫 번째 정수: <입력1>
  두 번째 정수: <입력2>
  (<작은값>, <큰값>)
"""

# [함수] (작은값, 큰값) 형태로 두 값을 반환
def getSorted(x, y):
    if x <= y:
        return x, y
    else:
        return y, x

# [입력] 프롬프트 메시지 없이 공백으로 구분된 두 정수 입력
a_str, b_str = input().split()
x, y = int(a_str), int(b_str)

# [처리] 다중 반환(튜플) → 언패킹으로 두 변수에 배치
a, b = getSorted(x, y)

# [출력] 요구 형식에 맞춰 세 줄 출력
print(f"첫 번째 정수: {x}")
print(f"두 번째 정수: {y}")
print((a, b))
