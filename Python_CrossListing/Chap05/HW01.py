# 실습결과보고서 문제 1번

"""
1. 사용자로부터 공백으로 구분된 정수들을 한 줄로 입력받아 리스트로 변환한 뒤,
사용자 정의 함수 find_max(nums)로 최댓값을 계산하여 출력하시오.
(입력 프롬프트 메시지 없이 입력 받음, 출력 형식은 예시대로 두 줄)
"""

# [함수] 리스트에서 최댓값을 찾아 반환
# - 내장함수 max()는 사용하지 않고 반복문으로 직접 구현
# - 아이디어: 첫 원소를 '현재 최댓값'으로 놓고, 나머지를 순차 비교하며 더 크면 교체
def find_max(nums):
    m = nums[0]            # 현재까지의 최댓값(초깃값: 첫 원소)
    for x in nums[1:]:     # 두 번째 원소부터 끝까지 순회
        # 현재 원소 x가 m보다 크면 m 갱신
        if x > m:
            m = x
    return m               # 최종 최댓값 반환


# [입력] 프롬프트 메시지 없이 공백으로 구분된 정수들을 한 줄로 입력받음
# 예) 1 5 32 3 2 3
raw = input()

# [전처리] 공백 분리 → 각 토큰을 int로 변환 → 정수 리스트 완성
parts = raw.split()            # 공백 기준 분리
nums = [int(p) for p in parts] # 문자열 → 정수 변환

# [출력] 요구 형식에 맞춰 두 줄로 출력
# 1) "입력 리스트: [...]"
# 2) "최댓값: <정수>"
print(f"입력 리스트: {nums}")
print(f"최댓값: {find_max(nums)}")
