# 실습결과보고서 문제 3번
"""
3. 원의 면적을 계산하는 함수 calcArea(radius)를 정의하고 테스트하시오.
- 반지름이 주어지지 않으면 5.0으로 간주한다(디폴트 인자 사용).
- 함수는 한 번은 매개변수를 넣은 상태, 한 번은 생략한 상태로 호출한다.
- 출력은 두 줄로, 각 면적 값을 그대로 출력한다.
"""

# [모듈] 원의 면적 계산을 위해 원주율 pi 사용
from math import pi

# [함수] calcArea(radius=5.0)
# - 공식: π * r^2
# - radius가 전달되지 않으면 기본값 5.0을 사용
def calcArea(radius=5.0):
    return pi * (radius ** 2)

# [테스트] 문제 요구대로 두 번 호출
# 1) 매개변수 전달(예: 10.0)
# 2) 매개변수 생략(기본값 5.0 적용)
print(calcArea(10.0))   # 314.1592653589793
print(calcArea())       # 78.53981633974483
