# 실습결과보고서 문제 1번

"""
1. 사용자로부터 문자열을 입력받고, 각 문자별로 개수를 세어 출력하는 프로그램을 작성하시오.
"""

# [입력] 한 줄 문자열을 받아서 문자 빈도를 계산할 대상이 됨
s = input("문자열을 입력하시오: ")

# [상태] 서로 대응하는 두 리스트:
# - chars[i]  : i번째로 '등장한' 문자 (등장 순서 유지)
# - count[i]  : 해당 문자의 누적 개수
chars = []  # 문자 리스트
count = []  # 개수 리스트

# [처리] 문자열을 왼쪽부터 하나씩 보며(등장 순서 유지)
for x in s:  # 문자열 순회
    ok = 0  # 현재 문자를 이미 본 적이 있는지(0=아님, 1=있음) 표시하는 플래그
    # 이미 등장한 문자 목록에서 선형 탐색으로 위치를 찾음
    for i in range(len(chars)):  # 인덱스로 확인
        if chars[i] == x:              # 이미 본 문자라면
            count[i] = count[i] + 1    # 해당 문자의 개수만 1 증가
            ok = 1
            break                       # 찾았으므로 더 볼 필요 없음
    # 한 번도 못 찾았으면(=새 문자면) 맨 뒤에 추가하고 개수는 1로 시작
    if ok == 0:  # 없으면 새로 추가
        chars.append(x)
        count.append(1)

# [출력] 처음 등장한 순서대로 "문자: 개수" 형태로 모두 출력
for i in range(len(chars)):  # 결과 출력
    print(chars[i], ":", count[i])
