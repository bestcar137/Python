# 실습결과보고서 문제 4번

"""
4. 다음 수열의 합을 계산하는 프로그램을 작성하시오.
[ 1/3 + 3/5 + 5/7 + ... + 99/101 ]
"""

s = 0.0                        # 누적 합(실수). 분수 합이므로 float로 시작합니다.
for n in range(1, 100, 2):     # n = 1, 3, 5, ..., 99 (range의 끝 100은 미포함)
    s += n / (n + 2)           # 각 항 n/(n+2)를 누적 더합니다.
print(f"주어진 수열의 합: {s}")  # 최종 합을 출력합니다.
