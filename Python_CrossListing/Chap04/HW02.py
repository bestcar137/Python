# 실습결과보고서 문제 2번

"""
2. 사용자가 선택한 로또 번호 6개와 보너스 번호 1개를 입력받아, 랜덤으로 생성된 로또 당첨 번호와 비교하여 등수를 출력하는 프로그램을 작성하시오.
6개 전부 일치 – "당첨 결과: 1등입니다! (6개 일치)"
5개 일치 + 보너스 번호 일치 – "당첨 결과: 2등입니다! (5개 일치 + 보너스 번호 일치)"
5개 일치 – "당첨 결과: 3등입니다! (5개 일치)"
4개 일치 – "당첨 결과: 4등입니다! (4개 일치)"
3개 일치 – "당첨 결과: 5등입니다! (3개 일치)"
2개 이하 일치 - "당첨 결과: 꽝입니다! (2개 이하 숫자 일치)
"""

from random import randint  # 난수 생성 함수만 가져와 사용

# 입력 -----------------------------------------------------------
# 한 줄에 '숫자 6개'를 공백으로 입력받아 정수 리스트로 변환
user_nums = list(map(int, input("로또 번호 6개를 띄어쓰기로 구분하여 입력하세요: ").split()))
# 보너스 번호는 별도로 하나 더 입력
user_bonus = int(input("보너스 번호를 입력하세요: "))

# 당첨 번호 생성(1~45, 중복 없이 6개 + 보너스 1개) ----------------
num = []  # 본당첨 번호 6개 담을 리스트
while len(num) < 6:
    n = randint(1, 45)  # 1~45에서 임의 추출
    if n not in num:  # 중복 방지
        num.append(n)

bonus_num = randint(1, 45)  # 보너스 번호는 본당첨과 달라야 함
while bonus_num in num:
    bonus_num = randint(1, 45)

# 생성된 당첨 번호 확인 출력 ---------------------------------------
print(f"랜덤 생성 로또 번호: {num}")
print(f"보너스 번호: [{bonus_num}]")

# 사용자와의 일치 개수 계산 ---------------------------------------
match = 0
for x in user_nums:
    if x in num:  # 사용자가 고른 번호가 본당첨 리스트에 있으면 카운트
        match += 1

# 보너스 일치 여부 (2등 판정에 필요) -------------------------------
bonus_hit = (user_bonus == bonus_num)

# 등수 판정 --------------------------------------------------------
if match == 6:
    print("당첨 결과: 1등입니다! (6개 일치)")
elif match == 5 and bonus_hit:
    print("당첨 결과: 2등입니다! (5개 일치 + 보너스 번호 일치)")
elif match == 5:
    print("당첨 결과: 3등입니다! (5개 일치)")
elif match == 4:
    print("당첨 결과: 4등입니다! (4개 일치)")
elif match == 3:
    print("당첨 결과: 5등입니다! (3개 일치)")
else:
    print("당첨 결과: 꽝입니다! (2개 이하 숫자 일치)")
